# exemplo de strings

nome = "João da Silva"
curso = "Tecnologia em de desenvolvimento de sistemas"

print(type(nome))
print(type(curso))


# acessar um elemento (caractere) da string

print(nome[5])

# acessar um "pedaço" (conj. de caracteres) da string

print(curso[0:5])

# concatenacao de string

nome = "Daniel"
